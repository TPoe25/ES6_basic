#!/usr/bin/env python3
"""
0-add.py
Module that contains function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats together.

    Args:
    a (float): First number to add.
    b (float): Second number to add.

    Returns:
    - float: Sum of a and b.
    """
    return a + b
