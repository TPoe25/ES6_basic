#!/usr/bin/env python3
"""
Module contains a type-annotatated function to concat two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Concats two strings

    Args:
        str1 (str): first string
        str2 (str): second string

    Returns:
        str: concat string
    """
    return str1 + str2
